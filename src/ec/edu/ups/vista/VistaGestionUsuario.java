    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String direccion = txtDireccion.getText();
        
        if (cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || direccion.isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "Llene todos los campos");
            
        } else {
            
            Persona p = new Persona(cedula, nombre, apellido, direccion);
            
            controladorTelefono.update(p, p);
            
            if (p != null) {
                
                listarPersonas(p);
                
            }
            
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        String cedula = txtCedula.getText();
        
        int eliminar = JOptionPane.showConfirmDialog(this, "Esta seguro de elimarlo");
        
        Persona p = new Persona(cedula);
        
        boolean cent = controladorTelefono.delete(p);
        
        if (cent && eliminar == JOptionPane.YES_OPTION) {
            
            datosDeLaTabla();
            
            JOptionPane.showMessageDialog(this, "Usuario eliminado");
            
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtrasActionPerformed
        
        limpiar();
        this.dispose();

    }//GEN-LAST:event_btnAtrasActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        String cedula = txtBuscarPorCedula.getText();
        
        if (cedula.isEmpty()) {
            
            limpiar();
            JOptionPane.showMessageDialog(this, "Llene el campo ");
            
        } else {
            
            Optional<Persona> persona = controladorTelefono.read(t -> cedula.equals(t.getCedula()));
            Persona p = persona.get();
            
            if (p != null) {
                
                listarPersonas(p);
                
            } else {
                
            }
            
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String direccion = txtDireccion.getText();
        
        if (cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || direccion.isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "Llene todos los campos");
            
        } else {
            
            Persona p = new Persona(cedula, nombre, apellido, direccion);
            
            controladorTelefono.create(p);
            
            JOptionPane.showMessageDialog(this, "El usuario se ha registrado correctamente");
            
        }
        

    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MouseClicked
        
        int fila = tblDatos.getSelectedRow();
        
        String cedula = (String) tblDatos.getValueAt(fila, 0);
        String nombre = (String) tblDatos.getValueAt(fila, 1);
        String apellido = (String) tblDatos.getValueAt(fila, 2);
        String direccion = (String) tblDatos.getValueAt(fila, 3);
        
        txtCedula.setText(cedula);
        txtNombre.setText(nombre);
        txtApellido.setText(apellido);
        txtDireccion.setText(direccion);

    }//GEN-LAST:event_MouseClicked

