    public void listarPersonas(Persona persona) {
        
        DefaultTableModel modelo = (DefaultTableModel) tblDatos.getModel();
        modelo.setRowCount(0);
        
        Object[] objeto = {persona.getCedula(), persona.getNombre(), persona.getApellido(),
            persona.getDireccion()};
        
        modelo.addRow(objeto);
        
        tblDatos.setModel(modelo);
        
    }
    
    
    public void datosDeLaTabla() {
        
        DefaultTableModel modelo = (DefaultTableModel) tblDatos.getModel();
        modelo.setRowCount(0);
        
        List<Persona> lista = controladorTelefono.findAll();
        
        TableRowSorter<TableModel> sorte = new TableRowSorter<>(modelo);
        tblDatos.setRowSorter(sorte);
        
        List<SortKey> sortKeys = new ArrayList<>();
        sortKeys.add(new SortKey(2, SortOrder.ASCENDING));
        sorte.setSortKeys(sortKeys);
        
        for (Persona persona : lista) {
            
            Object[] objeto = {persona.getCedula(), persona.getNombre(), persona.getApellido(),
                persona.getDireccion()};
            
            modelo.addRow(objeto);
            
            tblDatos.setModel(modelo);
            
        }
        
    }

