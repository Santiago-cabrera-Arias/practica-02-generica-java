    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        String numero = txtNumero.getText();
        String operadora = txtOperadora.getText();
        String tipo = txtTipo.getText();

        if (numero.isEmpty() || operadora.isEmpty() || tipo.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene los campos");

        } else {

            Telefono te = new Telefono(numero, operadora, tipo);

            controladorTelefono.update(te, te);

            if (te != null) {

                listarTabla(te);

            }

        }


    }//GEN-LAST:event_btnActualizarActionPerformed

