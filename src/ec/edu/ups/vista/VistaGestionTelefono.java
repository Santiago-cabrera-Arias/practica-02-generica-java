    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        String numero = txtNumero.getText();
        int eliminar = JOptionPane.showConfirmDialog(this, "Estas seguro de eliminarlo");

        if (numero.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Ingrese el numero");

        } else {

            Telefono t = new Telefono(numero);

            boolean cent = controladorTelefono.delete(t);

            if (cent && eliminar == JOptionPane.YES_OPTION) {

                datosDeLaTabla();
                JOptionPane.showMessageDialog(this, "dato eliminado");

            }

        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String numero = txtBuscarPorNumero.getText();

        if (numero.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene el campo");

        } else {

            Optional<Telefono> telefono = controladorTelefono.read(t -> numero.equals(t.getNumero()));
            Telefono t = telefono.get();

            if (t != null) {

                listarTabla(t);

            }

        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        String numero = txtNumero.getText();
        String operadora = txtOperadora.getText();
        String tipo = txtTipo.getText();

        if (numero.isEmpty() || operadora.isEmpty() || tipo.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene los campos");

        } else {

            Telefono telefono = new Telefono(numero, operadora, tipo);

            controladorTelefono.create(telefono);

            JOptionPane.showMessageDialog(this, "Se ha registrado el telefono");

        }


    }//GEN-LAST:event_btnRegistrarActionPerformed

